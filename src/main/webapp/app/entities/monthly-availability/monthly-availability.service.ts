import axios from 'axios';

import { IMonthlyAvailability } from '@/shared/model/monthly-availability.model';

const baseApiUrl = 'api/monthly-availabilities';

export default class MonthlyAvailabilityService {
  public find(id: number): Promise<IMonthlyAvailability> {
    return new Promise<IMonthlyAvailability>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IMonthlyAvailability): Promise<IMonthlyAvailability> {
    return new Promise<IMonthlyAvailability>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IMonthlyAvailability): Promise<IMonthlyAvailability> {
    return new Promise<IMonthlyAvailability>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IMonthlyAvailability): Promise<IMonthlyAvailability> {
    return new Promise<IMonthlyAvailability>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
